#creando tuplas con tuple

tupla = tuple(["dato1", "dato2"])

#creando tuplas sin paréntesis y multiples datos

tupla2 = "dato1", "dato2"

#creando tuplas con un solo dato y sin paréntesis

tupla3 = "dato1", #sin la coma se convierte en un string

#imprimiendo el tipo de dato de las tuplas

print(type(tupla))
print(type(tupla2))
print(type(tupla3))

